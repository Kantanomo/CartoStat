     ShotsFired INT UNSIGNED DEFAULT 0,
    ShotsHit INT UNSIGNED DEFAULT 0,
    Accuracy INT UNSIGNED DEFAULT 0,
    HeadShots INT UNSIGNED DEFAULT 0,
    BestSpree INT UNSIGNED DEFAULT 0,
    TimeAlive INT UNSIGNED DEFAULT 0,
    FlagScores INT UNSIGNED DEFAULT 0,
    FlagSteals INT UNSIGNED DEFAULT 0,
    FlagSaves INT UNSIGNED DEFAULT 0,
    FlagUnk INT UNSIGNED DEFAULT 0,
    BombScores INT UNSIGNED DEFAULT 0,
    BombKills INT UNSIGNED DEFAULT 0,
    BombGrabs INT UNSIGNED DEFAULT 0,
    BallScore INT UNSIGNED DEFAULT 0,
    BallKills INT UNSIGNED DEFAULT 0,
    BallCarrierKills INT UNSIGNED DEFAULT 0,
    KingKillsAsKing INT UNSIGNED DEFAULT 0,
    KingKilledKings INT UNSIGNED DEFAULT 0,
    JuggKilledJuggs INT UNSIGNED DEFAULT 0,
    JuggKillsAsJugg INT UNSIGNED DEFAULT 0,
    JuggTime INT UNSIGNED DEFAULT 0,
    TerrTaken INT UNSIGNED DEFAULT 0,
    TerrLost INT UNSIGNED DEFAULT 0,
    PRIMARY KEY(XUID)
) Engine=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;
