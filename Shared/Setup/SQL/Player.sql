    XUID BIGINT UNSIGNED,
    Gamertag VARCHAR(16),
    PrimaryColor TINYINT,
    SecondaryColor TINYINT,
    PrimaryEmblem TINYINT,
    SecondaryEmblem TINYINT,
    EmblemForeground TINYINT,
    EmblemBackground TINYINT,
    EmblemToggle TINYINT,
    PlayerModel TINYINT,
    Nameplate TINYINT,
    Wins INT UNSIGNED DEFAULT 0,
    Loss INT UNSIGNED DEFAULT 0,
    Kills INT UNSIGNED DEFAULT 0,
    Assists INT UNSIGNED DEFAULT 0,
    Betrayals INT UNSIGNED DEFAULT 0,
    Deaths INT UNSIGNED DEFAULT 0,
